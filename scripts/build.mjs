/**
 * Bundle userscript/ui-main.js (+ lib/*) into a single Tampermonkey userscript via esbuild IIFE.
 * Version is read from package.json so header / UI stay in sync.
 *
 * Install / auto-update URLs point at the `release` branch so pushes to `main`
 * do not auto-ship to Tampermonkey users.
 */
import esbuild from 'esbuild'
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.resolve(__dirname, '..')

const entryPath = path.join(root, 'userscript/ui-main.js')
const outPath = path.join(root, 'userscript/ccswitch-linuxdo-importer.user.js')
const pkgPath = path.join(root, 'package.json')

const pkg = JSON.parse(fs.readFileSync(pkgPath, 'utf8'))
const version = String(pkg.version || '0.0.0')
const rawBase =
  'https://raw.githubusercontent.com/Super-YYQ/ccswitch-linuxdo-importer/release/userscript/ccswitch-linuxdo-importer.user.js'

const header = `// ==UserScript==
// @name         CC Switch Importer for linux.do
// @namespace    https://github.com/Super-YYQ/ccswitch-linuxdo-importer
// @version      ${version}
// @description  选中 linux.do 分享文本，一键导入 CC Switch（Claude Code / Codex，自动识别模型）
// @author       CC Switch Importer Contributors
// @match        https://linux.do/*
// @match        https://www.linux.do/*
// @grant        GM_setClipboard
// @grant        GM_notification
// @run-at       document-idle
// @noframes
// @license      MIT
// @homepageURL  https://github.com/Super-YYQ/ccswitch-linuxdo-importer
// @supportURL   https://github.com/Super-YYQ/ccswitch-linuxdo-importer/issues
// @downloadURL  ${rawBase}
// @updateURL    ${rawBase}
// ==/UserScript==

/* eslint-disable */
/* generated by scripts/build.mjs — edit userscript/ui-main.js + lib/* then rebuild */
`

await esbuild.build({
  entryPoints: [entryPath],
  bundle: true,
  format: 'iife',
  platform: 'browser',
  target: ['es2018'],
  outfile: outPath,
  banner: { js: header },
  define: {
    __SCRIPT_VERSION__: JSON.stringify(version),
  },
  logLevel: 'info',
})

const out = fs.readFileSync(outPath, 'utf8')
if (!out.includes(`// @version      ${version}`)) {
  throw new Error('build output missing version header')
}
if (!out.includes('/release/userscript/ccswitch-linuxdo-importer.user.js')) {
  throw new Error('build output must point @updateURL at release branch')
}
if (/^export\s/m.test(out) || /\bexport\s+(function|const|class|default|\{)/.test(out)) {
  throw new Error('build output still contains export statements')
}

console.log('Wrote', outPath, `(v${version})`)
